    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String em = jTextField4.getText();
        String dbms = jTextField5.getText();
        String os = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","anki_ta@1234");
            
            Statement st = con.createStatement();
            int rowsUpdated = st.executeUpdate("UPDATE result SET physics = '" + physics + "', maths = '" + maths + "', em = '" + em + "', dbms = '" + dbms + "', os = '" + os + "' WHERE rollno = '" + roll + "'");
            
            if (rowsUpdated > 0) {
              JOptionPane.showMessageDialog(null, "Result updated!");
              setVisible(false);
              new editResult(roll).setVisible(true);
            }else {
              JOptionPane.showMessageDialog(null, "No rows were updated.");
            } 
            st.close();
            con.close();
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

