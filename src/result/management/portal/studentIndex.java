    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField4.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","anki_ta@1234");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            
            if(rs.next()) {
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField5.setText(rs.getString(6));
                jTextField6.setText(rs.getString(5));
                physics.setText(rs.getString(8));
                maths.setText(rs.getString(9));
                em.setText(rs.getString(10));
                dbms.setText(rs.getString(11));
                os.setText(rs.getString(12));
                
                int s1 = Integer.parseInt(physics.getText());
                int s2 = Integer.parseInt(maths.getText());
                int s3 = Integer.parseInt(em.getText());
                int s4 = Integer.parseInt(dbms.getText());
                int s5 = Integer.parseInt(os.getText());
                
                String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                
                jTextField12.setText(totalMarks);
                
                
            }
            
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
        
    }//GEN-LAST:event_formComponentShown

